long	ft_atol(const char *src)
{
	long int	nb;
	int			power;
	int			i;

	nb = 0;
	i = 0;
	power = 1;
	while (src[i] == ' ')
		i++;
	if (src[i] == '+' || src[i] == '-')
	{
		if (src[i] == '-')
			power = power * -1;
		i++;
	}
	while (src[i] >= '0' && src[i] <= '9')
	{
		nb = nb * 10 + src[i] - '0';
		i++;
	}
	return (nb * power);
}
